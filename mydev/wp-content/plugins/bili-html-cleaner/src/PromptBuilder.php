<?php

namespace BiliHtmlCleaner;

if ( ! defined( 'ABSPATH' ) ) {
	exit;
}

class PromptBuilder {
	public function build( string $markdown ): string {
		$markdown = trim( $markdown );

		if ( '' === $markdown ) {
			return '';
		}

		$parts = array(
			'你将收到一份从 Bilibili 页面 HTML 中提取并初步转换得到的 Markdown 正文。',
			'这类内容通常来自东方 Project 相关的创作接力活动页面，可能包含活动说明、参与者名单、作品标题、分工、发布时间、接力顺序、投稿链接、结果汇总等信息。',
			'你的任务是：在不编造信息、不修改事实的前提下，将它整理成更清晰、可读、适合发布或录入的 Markdown 文本。',
			'',
			'请严格遵守以下规则：',
			'',
			'1. 只输出纯 Markdown，不要写解释、说明、前言、后记。',
			'2. 保留所有链接，不要修改 URL，不要删除有效链接。',
			'2.1. 如果链接明显是 Bilibili 站内的视频、动态、专栏、音频或作品分享链接，也必须保留，因为它们很可能对应接力活动中的作品入口、投稿页面或成果展示页面。',
			'3. 如果原文中已有图片 Markdown，就保留；如果原文没有图片，就不要自行补图。',
			'4. 保留并尽量优化原有结构，包括标题、列表、加粗、引用、链接等。',
			'5. 可以删除明显网页噪音、重复残留、无意义操作提示，但不要删除正文信息。',
			'6. 不要编造原文没有的信息；无法确定的内容不要猜测、不要补全。',
			'7. 当多个用户名、作品名、链接、说明文字连续出现时，请优先把它们理解为“活动参与信息列表”或“作品条目列表”，而不是普通连续段落。',
			'8. 要特别注意区分不同类型的信息：',
			'   - 人名 / 用户名 / 社团名',
			'   - 作品标题',
			'   - 投稿链接 / 作品链接 / 跳转链接',
			'   - 分工、备注、附言',
			'   不要把这些不同字段错误拼接成一句自然语言。',
			'9. 如果能够较明确地识别出“一个参与者 + 对应作品/链接/备注”这样的条目结构，请整理成列表；如果能够识别出明显分组，也可以拆成小标题。',
			'10. 如果文中存在多个板块，例如“活动说明”“参与名单”“作品列表”“结果汇总”等，可以适度补充 Markdown 小标题或分割线（---）来增强结构，但只能在原文结构已经较明显时这样做，不要强行过度重构。',
			'11. 如果原文本身顺序混乱，可以在不改变事实对应关系的前提下，做轻微的断句、分段和排版优化。',
			'12. 不要把彼此相邻但无明确对应关系的人名、标题、链接错误配对；宁可分开列出，也不要错误合并。',
			'13. 如果某一行或某一组内容的归属不明确，优先保持原意并做保守整理，不要自作主张重组。',
			'14. 输出结果应尽量美观、整洁、层次清楚，适合直接给人阅读或继续录入网站。',
			'',
			'推荐的整理策略：',
			'- 活动总标题或总说明放在前面。',
			'- 若能识别出参与者条目，优先使用无序列表或有序列表。',
			'- 若能识别出明显分段，可使用二级或三级标题。',
			'- 若不同板块边界清晰，可使用分割线（---）增强可读性。',
			'- 对作品名、角色名、企划名等可以适度保留或补成加粗，但不要滥用。',
			'',
			'以下是待整理的 Markdown：',
			'```markdown',
			$markdown,
			'```',
		);

		return implode( "\n\n", $parts );
	}
}
